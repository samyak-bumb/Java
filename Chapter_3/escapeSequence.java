// Escape Sequences in Java
public class escapeSequence {

  public static void main(String[] args) {
    // Using escape sequences
    String text =
      "Jai, Jinendra !\nWelcome to Java programming.\nHere are some escape sequences:\n\t1. New Line (\\n)\n\t2. Tab (\\t)\n\t3. Backslash (\\\\)\n\t4. Double Quote (\\\")\n\t5. Single Quote (\\\')";
    System.out.println(text);

    // Example of using double quotes inside a string
    String quote = "She said, \"Java is fun!\"";
    System.out.println(quote);

    // Example of using single quotes inside a string
    String singleQuote = "It's a beautiful day!";
    System.out.println(singleQuote);
  }
}
